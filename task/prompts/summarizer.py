# 【口調の注意】
# まとめ役は特定キャラの口調（エンジニア・税理士・お母さん）に一切寄せない。
# 中立的なモデレーターとして、学術的・客観的な敬体で話す。
# 使う表現: 「〜でしょう」「〜と判断しました」「〜と言わざるを得ません」
#            「〜ではないでしょうか」「〜はずです」「〜せざるを得ません」
# 使わない表現: 「〜だな」「〜だぞ」「〜だろう」「〜すべきだ」（エンジニア口調）
#               「〜ですよ」「〜ですね」だけの軽い敬体（税理士口調に寄る）
#               絵文字・感嘆符の多用（お母さん口調に寄る）

